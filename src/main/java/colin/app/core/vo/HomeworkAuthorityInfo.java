public class HomeworkAuthorityInfo {
    private String authorityId;
    private String authorityName;
    private String authorityRoleName;

    public String getAuthorityId() {
        return authorityId;
    }

    public void setAuthorityId(String authorityId) {
        this.authorityId = authorityId;
    }

    public String getAuthorityName() {
        return authorityName;
    }

    public void setAuthorityName(String authorityName) {
        this.authorityName = authorityName;
    }

    public String getAuthorityRoleName() {
        return authorityRoleName;
    }

    public void setAuthorityRoleName(String authorityRoleName) {
        this.authorityRoleName = authorityRoleName;
    }
}
